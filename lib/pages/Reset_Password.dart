        // Notice that the counter didn't reset back to zero; the application
        // is not restarted.
        primarySwatch: Colors.blue,
      ),
      home: Scaffold(
        appBar: AppBar(
          title: const Text('Password Reset'),
        ),
        body: Center(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.center,
            mainAxisAlignment: MainAxisAlignment.center,
            children: <Widget>[
              Container(
                padding: const EdgeInsets.fromLTRB(20, 20, 20, 70),
                child: const FlutterLogo(
                  size: 40,
                ),
              ),
              Container(
                padding: const EdgeInsets.fromLTRB(20, 20, 20, 0),
                child: const Text('Enter the Email associated with your account for which you forgot your password.'),
              ),

              Container(
                padding: const EdgeInsets.fromLTRB(20, 20, 20, 0),
                child: TextField(
                  onChanged: (value) {
                    email = value;
                  },
                  decoration: InputDecoration(
                    border: OutlineInputBorder(
                      borderRadius: BorderRadius.circular(90.0),
                    ),
                    labelText: 'Email',
                  ),
                ),
              ),

              Container(
                  height: 80,
                  padding: const EdgeInsets.all(20),
                  child: ElevatedButton(
                    style: ElevatedButton.styleFrom(
                      minimumSize: const Size.fromHeight(50),
                    ),
                    child: const Text('Send Verification Code'),
                    onPressed: () async {Navigator.push(
                      context,
                      MaterialPageRoute(builder: (context) => const VeCode()),
                    );
                    try {
                      if(email.isEmpty){
                        msg="Enter your email";
                      }else if(email.isNotEmpty){
                        msg='No Account found for that email.';
                      }
                      var user = await _auth.sendPasswordResetEmail(email: email);
                    } catch (Error)
                    {AwesomeDialog(
                        context: context,
                        dialogType: DialogType.error,
                        animType: AnimType.rightSlide,
                        title: 'Error',
                        desc: msg ,
                    ).show();}
                    },
                  )
              ),
            ],
          ),
        ),
      ),
    );
  }
}

